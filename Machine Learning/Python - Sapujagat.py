"""# Pembuatan model

Berikut adalah penjelasan lengkap dari parameter yang digunakan pada proses training model YOLOv8:

| Parameter        | Deskripsi                                                                                   |
|------------------|---------------------------------------------------------------------------------------------|
| `YOLO('yolov8s.pt')` | Memuat model YOLOv8 versi **small** (`s`) dengan bobot pre-trained dari Ultralytics. Cocok untuk balancing antara akurasi dan kecepatan. |
| `data`           | Path ke file **`data.yaml`** yang berisi konfigurasi dataset, termasuk lokasi gambar, label, dan daftar nama kelas. |
| `epochs`         | Jumlah **epoch** atau iterasi penuh terhadap seluruh dataset selama proses training. Semakin besar â†’ potensi akurasi meningkat, tapi waktu training bertambah. |
| `imgsz`          | **Ukuran gambar** input. Semua gambar akan di-resize menjadi resolusi **640x640 piksel** sebelum masuk ke model. |
| `lr0`            | **Learning rate awal**. Mengatur seberapa besar perubahan bobot pada setiap iterasi training. Nilai terlalu besar â†’ training bisa tidak stabil; terlalu kecil â†’ training lambat. |
| `batch`          | Jumlah gambar yang diproses dalam satu batch selama training. Semakin besar batch â†’ semakin efisien (jika hardware mendukung), tapi butuh memori lebih banyak. |
| `warmup_epochs`  | Jumlah epoch awal untuk **warmup**. Warmup digunakan agar model belajar secara perlahan sebelum masuk ke learning rate normal â†’ mencegah model learning terlalu cepat di awal yang bisa menyebabkan overfitting atau loss tidak stabil. |
"""

model = YOLO('yolov8s.pt')

model.train(
    data=dataset.location + '/data.yaml',
    epochs=20,
    imgsz=640,
    lr0=0.01,
    batch=16,
    warmup_epochs=3,
)

model.val()

